import turtle

turtle.title("APLICAÇÕES INFORMÁTICAS B - TURTLE")
turtle.setup(400,400)

turtle.Turtle()

turtle.shape("turtle")
turtle.pensize(3)
turtle.color("red")

turtle.penup()
turtle.setpos(0,0)
turtle.pendown()
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)

turtle.penup()
turtle.color("Green")
turtle.setpos(-5,0)

turtle.pendown()
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)

turtle.penup()
turtle.color("blue")
turtle.setpos(0,-5)

turtle.pendown()
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)

turtle.penup()
turtle.color("yellow")
turtle.setpos(-5,-5)
turtle.pendown()

turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)

turtle.done()

