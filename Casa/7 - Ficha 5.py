op = int(input("Indique um número inteiro de 3 a 8: "))
if op == 3:
    print("3: Triângulo equilátero (3 lados iguais e 3 vértices)")
elif op == 4:
    print("4: Quadrado (4 lados iguais e 4 vértices)")
elif op == 5:
    print("5: Pentágono (5 lados iguais e 5 vértices)")
elif op == 6:
    print("6: Hexágono (6 lados iguais e 6 vértices)")
elif op == 7:
    print("7: Heptágono (7 lados iguais e 7 vértices)")
elif op == 8:
    print("8: Octógono (8 lados iguais e 8 vértices)")
else:
    print("Inseriu um número inválido")
