# programa que recebe dois numeros inteiros do utilizador e mostre a soma deles

print("\nFazer a soma de dois números inteiros")
n1 = int(input("Insira o primeiro número inteiro: "))
n2 = int(input("Insira o segundo número inteiro: "))
soma = n1+n2

print("O valor da soma do", n1, "com o", n2, "é", soma)

